valor = input('Digite algo')
print('o formato é:{}'.format(type(valor)));
print('é um numero?:{}'.format(valor.isnumeric()));
print('ta em maiusculo?:{}'.format(valor.isupper()));
print('só tem espaços?:{}'.format(valor.isspace()));
print('é alfabético?:{}'.format(valor.isalpha()));
print('Esta em maiuscula?:{}'.format(valor.isupper()));
print('Esta capitalizada?:{}'.format(valor.istitle()));
print('Está em minuscula??:{}'.format(valor.islower()));
